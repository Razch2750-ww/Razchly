export function useTranslation() {
  const language = useStore((state) => state.language) || 'id';
  
  const t = (key: string, variables?: Record<string, string | number>): string => {
    const keys = key.split('.');
    const dict = (translations[language as keyof typeof translations] || translations.id) as any;
    
    let result = dict;
    for (const k of keys) {
      if (result && typeof result === 'object' && k in result) {
        result = result[k];
      } else {
        // Fallback to Indonesian if key not found in current language
        const fallbackDict = translations.id as any;
        let fbResult = fallbackDict;
        for (const fbK of keys) {
          if (fbResult && typeof fbResult === 'object' && fbK in fbResult) {
            fbResult = fbResult[fbK];
          } else {
            fbResult = undefined;
            break;
          }
        }
        return fbResult !== undefined ? fbResult : key;
      }
    }
    
    if (typeof result !== 'string') {
      return key;
    }
    
    let text = result;
    if (variables) {
      Object.entries(variables).forEach(([vKey, vVal]) => {
        text = text.replace(new RegExp(`{${vKey}}`, 'g'), String(vVal));
      });
    }
    
    return text;
  };

  return { t, language };
}
